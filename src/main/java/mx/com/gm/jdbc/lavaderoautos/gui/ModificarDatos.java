    private void cargarDatos(int num_cliente) {
        this.coche = controladoraLogica.traerCoche(num_cliente);
        
        txtObservaciones.setText(coche.getObservaciones());
        txtNombreDuenio.setText(coche.getUnDuenio().getNombre());
        txtModelo.setText(coche.getModelo());
        txtMarca.setText(coche.getMarca());
        txtColor.setText(coche.getColor());
        txtCelDuenio.setText(coche.getUnDuenio().getCelDuenio());
        
        if(coche.getEsChocado().equals("SI")){
            cmbChocado.setSelectedIndex(1);
        }else if(coche.getEsChocado().equals("NO")) {
            cmbChocado.setSelectedIndex(2);
        }
        
        if(coche.getTratamientoEspecial().equals("SI")){
            cmbTratamiento.setSelectedIndex(1);
        }else if(coche.getEsChocado().equals("NO")) {
            cmbTratamiento.setSelectedIndex(2);
        }
        
        
        
    }
    
    private void mostrarMensaje(String mensaje, String tipo, String titulo) {
        
        JOptionPane optionPane = new JOptionPane(mensaje);
        if(tipo.equals("Info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }
        else if(tipo.equals("Error")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
