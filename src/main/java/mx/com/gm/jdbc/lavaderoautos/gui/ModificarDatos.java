    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String marca = txtMarca.getText();
        String modelo = txtModelo.getText();
        String color = txtColor.getText();
        String observaciones = txtObservaciones.getText();
        String chocado = (String) cmbChocado.getSelectedItem();
        String tratamiento = (String) cmbTratamiento.getSelectedItem();
        
        String nombreDuenio = txtNombreDuenio.getText();
        String celDuenio = txtCelDuenio.getText();
        
        controladoraLogica.modificarCoche(coche, marca, modelo, color, observaciones, chocado, tratamiento, nombreDuenio, celDuenio);
        
        mostrarMensaje("Edicion realizada correctamente", "Info", "Edicion correcta");
        
        VerDatos pantalla = new VerDatos();
        pantalla.setVisible(true);
        pantalla.setLocationRelativeTo(null);
        
        this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

    
